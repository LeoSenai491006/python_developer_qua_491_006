nome = input("Informe seu nome: ").title() # Primeira palavra de toda palavra maiúscula
print(f"Seu nome é: {nome}.")

texto = input("Digite uma frase: ").capitalize() # Somente a primeira palavra da frase maiúcula
print(f"Frase: {texto}.")

minuscula = input("Digite um texto totalmente em caixa baixa: ").upper() # converte todo o texto em maiúscula
print(f"Texto em maiúsculo: {minuscula}.")

maiuscula = input("Digite um texto totalmente em caixa alta: ").lower() # converte todo o texto em minúsculo
print(f"Texto em minúsculo: {maiuscula}.")